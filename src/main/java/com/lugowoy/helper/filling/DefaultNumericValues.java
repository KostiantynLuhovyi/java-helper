package com.lugowoy.helper.filling;

/**
 * The interface provides a set of constants that are used as default values to filling objects or storage(data structures).
 * <p>Created by Konstantin Lugowoy on 08-Jan-18.
 *
 * @author Konstantin Lugowoy
 * @version 1.0 (1.5)
 * @since 1.7.8
 */
public interface DefaultNumericValues {

    /**
     * The integer value by default.
     */
    int DEFAULT_INT_VALUE = 0;

    /**
     * The double value by default.
     */
    double DEFAULT_DOUBLE_VALUE = 0.0;

    /**
     * The integer zero value of the bound.
     */
    int INT_ZERO = 0;

    /**
     * The double zero value of the bound.
     */
    double DOUBLE_ZERO = 0d;

    /**
     * The integer value of the lower (negative) boundary by default.
     */
    int INT_LOWER_BOUND = -10;

    /**
     * The integer value of the upper (positive) boundary by default.
     */
    int INT_UPPER_BOUND = 10;

    /**
     * The double value of the lower (negative) boundary by default.
     */
    double DOUBLE_LOWER_BOUND = -10.0;

    /**
     * The double value of the upper (positive) boundary by default.
     */
    double DOUBLE_UPPER_BOUND = 10.0;

}
