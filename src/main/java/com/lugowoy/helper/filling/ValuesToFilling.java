package com.lugowoy.helper.filling;

/**
 * The interface contains a set of constants that used as default values
 * for filling objects, storages (data structures), etc.
 *
 * <p>Created by Konstantin Lugowoy on 08-Jan-18.
 *
 * @author Konstantin Lugowoy
 * @version 1.7
 * @since 1.7.8
 */
public interface ValuesToFilling {

    /**
     * The integer default value.
     */
    int DEFAULT_INT_VALUE = 0;

    /**
     * The double default value.
     */
    double DEFAULT_DOUBLE_VALUE = 0.0;

    /**
     * The integer zero value.
     */
    int INT_ZERO = 0;

    /**
     * The double zero value.
     */
    double DOUBLE_ZERO = 0d;

    /**
     * The integer default value of the lower (negative) boundary.
     */
    int INT_LOWER_BOUND = -10;

    /**
     * The integer default value of the upper (positive) boundary.
     */
    int INT_UPPER_BOUND = 10;

    /**
     * The double default value of the lower (negative) boundary.
     */
    double DOUBLE_LOWER_BOUND = -10.0;

    /**
     * The double default value of the upper (positive) boundary.
     */
    double DOUBLE_UPPER_BOUND = 10.0;

}
